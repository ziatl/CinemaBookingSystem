    private void tablePurchasseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePurchasseMouseClicked
        // TODO add your handling code here:
        if (tablePurchasse.getSelectedRow()!=-1) {
            System.err.println("Selction : "+tablePurchasse.getSelectedRow());
            BookTicket bookTicket = liste.get(tablePurchasse.getSelectedRow());
            dao = new BookingDaoImpl();
            listRecommandationModel = new ListRecommandationModel();
            tableRecommandation.setModel(listRecommandationModel);
            System.err.println("Movie : "+bookTicket.getOnScreen().getMovie().getTitle());
            System.err.println("Categorie : "+bookTicket.getOnScreen().getMovie().getCategorie().getName());
            listeOnScreens = dao.findOnScreenByCategorie(bookTicket.getOnScreen().getMovie().getCategorie().getName());             
            listRecommandationModel.LoadOnScreen(listeOnScreens);
            
        }
    }//GEN-LAST:event_tablePurchasseMouseClicked


